#!/usr/bin/env node

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  DEFAULT_LANGUAGE,
  PUBLIC_ROUTES,
  SUPPORTED_LANGUAGES,
  withLang,
} from '../src/seo/publicRoutesData.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const SITE_URL = 'https://koreanstudy.me';
const OUTPUT_PATH = join(__dirname, '..', 'docs', 'SEO_OUTREACH_PLAYBOOK.md');
const GUIDE_ROUTES = PUBLIC_ROUTES.filter(
  (route) => route.path.startsWith('/learn/') && route.path !== '/learn'
);

const LANGUAGE_LABELS = {
  en: 'English',
  zh: '中文',
  vi: 'Tiếng Việt',
  mn: 'Монгол',
};

function toIsoDate() {
  const now = new Date();
  const year = String(now.getFullYear());
  const month = String(now.getMonth() + 1).padStart(2, '0');
  const day = String(now.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

function stripBrandSuffix(title) {
  return String(title || '').replace(/\s*-\s*DuHan\s*$/u, '').trim();
}

function shorten(text, maxLength) {
  const value = String(text || '').trim();
  if (value.length <= maxLength) return value;
  return `${value.slice(0, Math.max(0, maxLength - 1)).trimEnd()}…`;
}

function escapeMdCell(value) {
  return String(value || '').replaceAll('|', '\\|').replaceAll('\n', '<br />');
}

function getIndexLanguages(route) {
  if (!Array.isArray(route?.indexLanguages) || route.indexLanguages.length === 0) {
    return [...SUPPORTED_LANGUAGES];
  }
  return route.indexLanguages;
}

function getLocalizedMeta(route, language) {
  const localized = language === DEFAULT_LANGUAGE ? null : route.metaByLang?.[language];
  return localized ? { ...route.meta, ...localized } : route.meta;
}

function getAnchorTexts(meta) {
  const keywordAnchors = String(meta.keywords || '')
    .split(',')
    .map((token) => token.trim())
    .filter(Boolean);
  const unique = [];
  const seen = new Set();
  for (const token of keywordAnchors) {
    const key = token.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    unique.push(token);
    if (unique.length >= 3) break;
  }
  if (unique.length > 0) return unique;
  return [stripBrandSuffix(meta.title)];
}

function buildChannelTemplates(title, description, url) {
  const cleanTitle = stripBrandSuffix(title);
  return {
    x: `${shorten(cleanTitle, 68)}\n${shorten(description, 120)}\n${url}`,
    redditTitle: shorten(cleanTitle, 110),
    redditBody: `${shorten(description, 280)}\n\nStudy link: ${url}`,
    community: `${cleanTitle} — ${shorten(description, 150)}\n${url}`,
  };
}

function buildGuideEntries() {
  const rows = [];
  for (const route of GUIDE_ROUTES) {
    const indexLanguages = getIndexLanguages(route);
    for (const language of indexLanguages) {
      const meta = getLocalizedMeta(route, language);
      const url = `${SITE_URL}${withLang(language, route.path)}`;
      const anchors = getAnchorTexts(meta);
      const channel = buildChannelTemplates(meta.title, meta.description, url);
      rows.push({
        language,
        languageLabel: LANGUAGE_LABELS[language] || language,
        path: route.path,
        title: meta.title,
        description: meta.description,
        url,
        anchors,
        channel,
      });
    }
  }
  return rows;
}

function renderMarkdown(entries) {
  const generatedAt = toIsoDate();
  const anchorRows = entries.flatMap((entry) =>
    entry.anchors.map((anchor) => ({
      language: entry.languageLabel,
      route: entry.path,
      anchor,
      url: entry.url,
    }))
  );

  const summaryLines = [
    '# SEO Outreach Playbook',
    '',
    `- Generated at: ${generatedAt}`,
    `- Target domain: ${SITE_URL}`,
    `- Guide routes: ${GUIDE_ROUTES.length}`,
    `- Localized entries: ${entries.length}`,
    '',
    '> This file is auto-generated by `scripts/generate-seo-outreach-assets.mjs`.',
    '',
    '## Anchor Text Matrix',
    '',
    '| Language | Route | Anchor Text | Target URL |',
    '| --- | --- | --- | --- |',
    ...anchorRows.map((row) =>
      `| ${escapeMdCell(row.language)} | ${escapeMdCell(row.route)} | ${escapeMdCell(row.anchor)} | ${escapeMdCell(row.url)} |`
    ),
    '',
    '## Channel Snippets',
    '',
  ];

  const entryBlocks = entries.flatMap((entry) => [
    `### ${entry.languageLabel} · ${entry.path}`,
    '',
    `- SEO Title: ${entry.title}`,
    `- SEO Description: ${entry.description}`,
    `- URL: ${entry.url}`,
    `- Suggested Anchors: ${entry.anchors.join(' · ')}`,
    '',
    '**X / Twitter**',
    '',
    '```text',
    entry.channel.x,
    '```',
    '',
    '**Reddit Title**',
    '',
    '```text',
    entry.channel.redditTitle,
    '```',
    '',
    '**Reddit Body / Forum Body**',
    '',
    '```text',
    entry.channel.redditBody,
    '```',
    '',
    '**Community Short Blurb**',
    '',
    '```text',
    entry.channel.community,
    '```',
    '',
  ]);

  return [...summaryLines, ...entryBlocks].join('\n');
}

function main() {
  const entries = buildGuideEntries();
  const content = renderMarkdown(entries);
  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, `${content}\n`, 'utf-8');
  console.log(`✅ Generated SEO outreach playbook: ${OUTPUT_PATH}`);
  console.log(`   - Entries: ${entries.length}`);
  console.log(`   - Anchor rows: ${entries.reduce((sum, item) => sum + item.anchors.length, 0)}`);
}

main();
